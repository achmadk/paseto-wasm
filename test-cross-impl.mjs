async function main() {
  console.log('=== Cross-Implementation Test: PHP → Rust/WASM ===\n');
  console.log('This verifies that tokens generated by PHP can be verified by Rust/WASM.\n');
  
  let testData;
  try {
    testData = JSON.parse(readFileSync('./test-data.json', 'utf8'));
  } catch (e) {
    console.log('❌ ERROR: Could not load test-data.json');
    console.log('Please run: php generate-test-tokens.php');
    console.log('\nFalling back to self-test mode...\n');
    
    await runSelfTests();
    return;
  }
  
  console.log('Testing V4 Local (PHP → Rust):');
  console.log('─────────────────────────────────');
  for (const test of testData.v4_local) {
    testV4LocalDecrypt(test);
  }
  
  console.log('\nTesting V4 Public (PHP → Rust):');
  console.log('─────────────────────────────────');
  for (const test of testData.v4_public) {
    testV4PublicVerify(test);
  }
  
  console.log('\nTesting V3 Local (PHP → Rust):');
  console.log('─────────────────────────────────');
  for (const test of testData.v3_local) {
    testV3LocalDecrypt(test);
  }
  
  console.log('\nTesting V3 Public (PHP → Rust):');
  console.log('─────────────────────────────────');
  for (const test of testData.v3_public) {
    testV3PublicVerify(test);
  }
  
  console.log('\n=== Summary ===');
  console.log(`Passed: ${results.passed}`);
  console.log(`Failed: ${results.failed}`);
  console.log(`Total: ${results.passed + results.failed}`);
  
  if (results.failed > 0) {
    console.log('\n⚠️  Some tests failed. Check errors above.');
    process.exit(1);
  } else {
    console.log('\n✅ All tests passed! PHP and Rust/WASM are compatible.');
  }
}

async function runSelfTests() {
  console.log('Running self-tests (Rust only)...\n');
  
  const tests = [
    { name: 'V4 Local', run: () => {
      const key = paseto.generate_v4_local_key();
      const message = '{"data":"test"}';
      const token = paseto.encrypt_v4_local(key, message, null, null);
      const decrypted = paseto.decrypt_v4_local(key, token, null, null);
      return decrypted === message;
    }},
    { name: 'V4 Public', run: () => {
      const kp = paseto.generate_v4_public_key_pair();
      const message = '{"data":"test"}';
      const token = paseto.sign_v4_public(kp.secret, message, null, null);
      const verified = paseto.verify_v4_public(kp.public, token, null, null);
      return verified === message;
    }},
    { name: 'V3 Local', run: () => {
      const key = pasetoV3.generate_v3_local_key();
      const message = '{"data":"test"}';
      const token = pasetoV3.encrypt_v3_local(key, message, null, null);
      const decrypted = pasetoV3.decrypt_v3_local(key, token, null, null);
      return decrypted === message;
    }},
    { name: 'V3 Public', run: () => {
      const kp = pasetoV3.generate_v3_public_key_pair();
      const message = '{"data":"test"}';
      const token = pasetoV3.sign_v3_public(kp.secret, message, null, null);
      const verified = pasetoV3.verify_v3_public(kp.public, token, null, null);
      return verified === message;
    }},
  ];
  
  for (const test of tests) {
    try {
      const passed = test.run();
      if (passed) {
        console.log(`  ✅ ${test.name}: PASS`);
        results.passed++;
      } else {
        console.log(`  ❌ ${test.name}: FAILED`);
        results.failed++;
      }
    } catch (e) {
      console.log(`  ❌ ${test.name}: ERROR - ${e}`);
      results.failed++;
    }
  }
  
  console.log(`\nSelf-test result: ${results.passed} passed, ${results.failed} failed`);
}

main().catch(console.error);
